package Day_15_Method_Overloading;

public class PrimeNumberPractice {

	public static void main(String[] args) {

	
		int row = 10;
		int column = 10;
		
		for(int i = 1; i<=row; i++ ) {
			for (int j=1;j<=column;j++) {
				System.out.print(i*j + " ");
			}
			System.out.println();
		}
		
		
		
		
	}
	

}
