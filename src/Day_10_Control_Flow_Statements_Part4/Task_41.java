package Day_10_Control_Flow_Statements_Part4;

public class Task_41 {

	public static void main(String[] args) {


		for(int i=5;i<=140;i+=2) {
			System.out.print(i + " ");
		}
		System.out.println("\n");
		
		for(int i=5;i<=140;i++) {
			if(i%2!=0) {
				System.out.print(i + " ");
			}
		}
		
		
		
	}

}
