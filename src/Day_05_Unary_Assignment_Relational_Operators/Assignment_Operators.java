package Day_05_Unary_Assignment_Relational_Operators;

public class Assignment_Operators {

	public static void main(String[] args) {


		int i=100;
		int x=4;
		x/=i;
		System.out.println(x);
		
		i+=1;
		System.out.println(i);
		
		i-=2;
		System.out.println(i);
		
		i*=-3;
		System.out.println(i);
		
		i/=25;
		System.out.println(i);
		
		i%=46;
		System.out.println(i);

	}

}
