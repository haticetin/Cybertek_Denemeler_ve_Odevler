package Week4ReviewSession;

public class Primitive_Nonprimitive {

	public static void main(String[] args) {
		
		byte byteValue = 35; //-128 to 127
		short shortValue = 20525;
		int intValue = 1205468;
		long longValue = 1234345585676L;
		
		boolean bool = true;
		
		float floatValue = 67.54f;
		double doubleValue = 67.54;
		
		char charValue = 'a';
		
		String str = "apple";
		
		System.out.println("byte: " + byteValue);
		System.out.println("short: " + shortValue);
		System.out.println("int: " + intValue);
		System.out.println("long: " + longValue);
		System.out.println("char: " + charValue);
		System.out.println("boolean: " + bool);
	}

}
